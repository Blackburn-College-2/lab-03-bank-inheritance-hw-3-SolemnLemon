package emptyjavaproject;

/**
 *
 * @author sunbe
 */
public class Currency {



    String name;
    String symbol;

    public Currency(String name, String symbol) {
        this.name = name;
        this.symbol = symbol;

    }
    
        public String getName() {
        return name;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

}
